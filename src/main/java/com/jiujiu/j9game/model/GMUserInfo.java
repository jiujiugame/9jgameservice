package com.jiujiu.j9game.model;

public class GMUserInfo {
    private Long id;

    private String account;

    private String uid;

    private String gid_list;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getGid_list() {
        return gid_list;
    }

    public void setGid_list(String gid_list) {
        this.gid_list = gid_list;
    }
}
